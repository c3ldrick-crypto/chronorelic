/**
 * Returns a pseudo-random quote seeded by the captured minute and date.
 * Same player, same minute + day → same quote (deterministic, but varies across captures).
 */
export function getQuoteForCapture(minute: string, date?: string): TimeQuote {
  const seed = `${minute}${date ?? ""}${TIME_QUOTES.length}`
  let hash = 0
  for (let i = 0; i < seed.length; i++) {
    hash = (hash * 31 + seed.charCodeAt(i)) >>> 0
  }
  return TIME_QUOTES[hash % TIME_QUOTES.length]
}

/** Returns a truly random quote (for client-side use). */
export function getRandomQuote(): TimeQuote {
  return TIME_QUOTES[Math.floor(Math.random() * TIME_QUOTES.length)]
}
